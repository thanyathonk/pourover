export const recipeMap = Object.fromEntries(
  recipeLibrary.map((r) => [r.id, r]),
) as Record<string, RecipeLibraryEntry>;

type ResolveRecipeInput = {
  dripper: DripperId;
  filter: FilterId;
  brewMode: BrewMode;
  flavorTarget?: FlavorTarget;
  roastLevel?: RoastLevel;
  process?: ProcessType;
  recipeId?: string;
};

/** Resolve recipe: explicit id > dripper+filter+brewMode+flavor > defaults */
export function resolveRecipe(input: ResolveRecipeInput): RecipeLibraryEntry {
  const { dripper, filter, brewMode, flavorTarget, roastLevel, recipeId } = input;

  if (recipeId && recipeMap[recipeId]) {
    const r = recipeMap[recipeId];
    if (r.dripper === dripper && r.filter === filter && r.brewMode === brewMode) {
      return r;
    }
  }

  const candidates = recipeLibrary.filter(
    (r) => r.dripper === dripper && r.filter === filter && r.brewMode === brewMode,
  );

  if (flavorTarget) {
    const byFlavor = candidates.find((r) => r.flavorBias === flavorTarget);
    if (byFlavor) return byFlavor;
  }

  const defaultIds: Record<string, string> = {
    "v60,hario-paper,hot": "v60-hario-hot-light-balanced-default",
    "v60,hario-paper,iced": "v60-hario-iced-3pour",
    "origami,origami-paper,hot": "origami-cone-hot-bright-default",
    "origami,kalita-wave,hot": "origami-wave-hot-sweet-default",
    "origami,origami-paper,iced": "origami-iced-3pour",
    "origami,kalita-wave,iced": "origami-iced-3pour",
    "ct62,hario-paper,hot": "ct62-hario-hot-balanced-default",
    "ct62,hario-paper,iced": "ct62-hario-iced-3pour",
  };

  const defaultId = defaultIds[`${dripper},${filter},${brewMode}`];
  if (defaultId && recipeMap[defaultId]) {
    return recipeMap[defaultId];
  }

  const byDripperBrewMode = recipeLibrary.filter(
    (r) => r.dripper === dripper && r.brewMode === brewMode,
  );
  return byDripperBrewMode[0] ?? recipeLibrary[0];
}
